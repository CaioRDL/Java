    private void jSpinner1qtdeStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1qtdeStateChanged
        try
        {
            int qtde = (Integer) jSpinner1qtde.getValue();
        
            if(qtde == 0)
            {
                JOptionPane.showMessageDialog(rootPane, "Quantidade não pode ser 0");
            }
            else
            {
                float total = qtde*Float.parseFloat(jTextField2preco.getText().replace(",", "."));
                jTextField2total.setText(String.valueOf(total).replace(".",","));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jSpinner1qtdeStateChanged

