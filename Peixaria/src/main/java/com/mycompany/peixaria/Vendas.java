    private void jButton1venderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1venderActionPerformed
        // TODO add your handling code here:
        
        String prod =  jComboBox1Produto.getSelectedItem().toString();
        String qtde = jSpinner1qtde.getValue().toString();
        float preco = Float.parseFloat(jTextField2preco.getText().replace(",", "."));
        float valor_Total = Float.parseFloat(jTextField2total.getText().replace(",", "."));
        
        
        if (valor_Total > 0 && jComboBox1Produto.getSelectedIndex() >= 0) {

            caixa += valor_Total;
            
            jTextPane2caixa.setText(String.valueOf(caixa));
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            Object[] row = {prod, preco, qtde, valor_Total, caixa};
            model.addRow(row);
            
        }
        
    }//GEN-LAST:event_jButton1venderActionPerformed

